package DaysProgram.DaysProgram;

public class RepeatStringChar {

	public static void main(String[] args) {
		String a="Kani";
		char one=a.charAt(0);
		char two=a.charAt(1);
		char three=a.charAt(2);
		char four=a.charAt(3);
		System.out.println(one +" "+ two +" " + three+ " " + four + " ");
		

	}

}
